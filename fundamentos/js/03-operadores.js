const resta = 3 - 2; // - resta
const multiplicacion = 4 * 2; // * multiplicacion
const division = 5 / 3; // / division
const exponente = 2 ** 3; // ** exponente o potenci
const moduloOResto = 5 % 2; // % modulo o resto

let x = 5;
let y = 5;

x++; // Incrementa 1
y--; // Decrementa 1

x += 1; // x = 5 + 1
